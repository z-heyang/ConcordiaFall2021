package ca.concordia.cejv416.fall2021.Class_05;

public class if_else_if {

    public static void main(String[] args) {

        int numPPL = 20;

        if (numPPL < 50)
        {
            System.out.println("A");
        } else if (numPPL < 100)
        {
            System.out.println("B");
        }
        else
        {
            System.out.println("C");
        }
    }
}
